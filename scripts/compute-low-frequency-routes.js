#!/usr/bin/env node
// Compute the `lowFrequency` route list for src/bus/routes.js — routes with
// median weekday daytime (6 AM–10 PM) headway > 15 min, the seam where the
// curated `gaps`/`ghosts` detector coverage ends. Re-run after a GTFS refresh
// to regenerate; paste the printed array into routes.js.

const Path = require('node:path');
const { loadIndex } = require('../src/shared/gtfs');
const { allRoutes } = require('../src/bus/routes');

const CUTOFF_MIN = 15;

function medianHeadway(byDir) {
  const perDir = [];
  for (const dirInfo of Object.values(byDir || {})) {
    const hw = dirInfo.headways?.weekday;
    if (!hw) continue;
    const vals = [];
    for (let h = 6; h <= 22; h++) {
      const v = hw[h];
      if (v != null && Number.isFinite(v)) vals.push(v);
    }
    if (vals.length) {
      vals.sort((a, b) => a - b);
      perDir.push(vals[Math.floor(vals.length / 2)]);
    }
  }
  if (!perDir.length) return null;
  return Math.min(...perDir);
}

function main() {
  const idx = loadIndex();
  const eligible = [];
  for (const r of allRoutes) {
    const hw = medianHeadway(idx.routes[r]);
    if (hw != null && hw > CUTOFF_MIN) eligible.push({ route: r, hw });
  }
  // Sort by route number to match the ordering convention in routes.js.
  eligible.sort((a, b) => {
    const an = parseInt(a.route, 10);
    const bn = parseInt(b.route, 10);
    if (an !== bn) return an - bn;
    return a.route.localeCompare(b.route);
  });
  console.log(
    `// ${eligible.length} routes with median weekday daytime headway > ${CUTOFF_MIN} min`,
  );
  console.log(
    `// Generated by scripts/compute-low-frequency-routes.js (${Path.basename(__filename)})`,
  );
  console.log('const lowFrequency = [');
  for (const { route, hw } of eligible) {
    console.log(`  '${route}', // ${hw.toFixed(1)} min`);
  }
  console.log('];');
}

main();
